public class Operadores {
    public static void main(String[] args) {
        int primeiroNumero = 10;
        int segundoNumero = 4;

        int aumentarPrimeiroNumero = ++primeiroNumero;
        int diminuirSegundoNumero = --segundoNumero;

        System.out.println(aumentarPrimeiroNumero);
        System.out.println(diminuirSegundoNumero);
    }
    
}
